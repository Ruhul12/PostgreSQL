#Variables
age = int(input("Enter Your Age: "))

#calculate how many days you passed in your life
passed_days = age * 365
print(f"Your Passing Days Of Your Life Is {passed_days}")

#calculate how many second you passed in your life
passed_second = passed_days * 24 * 60 * 60
print(f"Your Passing Days Of Your Life Is {passed_second}")